""" 
Python =  Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas"""

# Aspas Simples
print('Wellington Gomes')

# Aspas duplas
print("Wellington Gomes")

# Escape
print("Wellington \"Gomes\"") 
"""Comando escape é utilizado para que se utilize caracteres diferentes para 
serem ou não mostrados, usando a \ invertida para executar"""

# r
print(r"Wellington \"Gomes\"") 

""" --> A regra do escape e do r são pouco utilizada na programação porque deixam o codigo 
muito cheio, abaixo será mostrado um metodo mais eficaz para deixar o codigo mais CLEAN <-- """

"""Ao apagar o escape\ e substituindo por aspas simples/dupla, acaba que tem a mesma 
função e deixa seu codigo bem mais clean. Exemplos abaixo"""

# Aspas duplas
print(1, 'Wellington "Gomes"')

# Escape
print(2, "Wellington '\Gomes'")

