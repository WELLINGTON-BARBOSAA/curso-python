# Escreva um programa para ler as dimensões de um retângulo (base e altura),
# calcule e apresente a área.

base_triangulo = float(input('Digite o tamanho da base: '))
altura_triangulo = float(input('Digite o tamanho da altura: '))

area_calculada = (base_triangulo * altura_triangulo) /2

print(f'Base do triangulo é de :{base_triangulo}')
print()
print(f'Altura do triangulo é de :{altura_triangulo}')
print()
print(f'E o valor da sua area é de : {area_calculada}cm')
